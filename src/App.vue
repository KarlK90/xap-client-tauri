          </q-avatar>
          QMK XAP GUI
        </q-toolbar-title>
        <q-select v-model="current_id" :options="ids" label="Device" />
      </q-toolbar>

      <q-tabs align="left">
        <q-route-tab to="/page1" label="Device" />
        <q-route-tab to="/page2" label="Keymap" />
        <q-route-tab to="/page3" label="RGBLight" />
      </q-tabs>
    </q-header>

